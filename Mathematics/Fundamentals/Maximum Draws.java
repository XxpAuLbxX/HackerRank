import java.io.*;
import java.util.*;
import java.text.*;
import java.math.*;
import java.util.regex.*;

public class Solution {

    public static void main(String[] args) {
        Scanner scan = new Scanner(System.in);
        int T = scan.nextInt(), pairs;
        for(int i = 0; i < T; i++) {
            pairs = scan.nextInt();
            System.out.println(pairs+1);
        }
    }
}